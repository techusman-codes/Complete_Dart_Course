void main() {
  // The var Key Word

  var name = 'Usman';
  name = 'Umar';
  print(name);

  // The final key Word
  final surName = 'Garba';
  print(surName);

  // The const key Word
  const lastName = 'Peters';
  print(lastName);
  // The dynamic Keyword
  dynamic temp = '10';
  temp = 10;
  print(temp);
}
